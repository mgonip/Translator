    String spr(String txt) {
        if (txt.contains("‼")) {
            return "BRAK";
        }
        return txt;
    }

    String odspr(int od) {
        if (od == 1) {
            return "M";
        } else if (od == 2) {
            return "D";
        } else if (od == 3) {
            return "C";
        } else if (od == 4) {
            return "B";
        } else if (od == 5) {
            return "N";
        } else if (od == 6) {
            return "MSC";
        } else if (od == 7) {
            return "W";
        }
        return "";
    }

    String lspr(int l) {
        if (l == 1) {
            return "LP";
        } else if (l == 2) {
            return "LM";
        }
        return "";
    }

